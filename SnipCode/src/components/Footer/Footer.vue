  methods:
  {
    handleProfileAccess()
    {
      if(this.token !== undefined && this.token !== null)
      {
          this.$router.push("/profile");
      }
      else
      {
          this.$router.push("/auth/login");
      }
    },
    handleLogout()
    {
      if(this.token !== undefined && this.token !== null)
      {
        this.$store.dispatch("logout").then(() => 
